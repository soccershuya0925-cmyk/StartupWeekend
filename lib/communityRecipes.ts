/** Fisher-Yates シャッフル（毎回違う順番でカードを出す） */
export function shuffleRecipes(recipes: CommunityRecipe[]): CommunityRecipe[] {
  const arr = [...recipes];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}
